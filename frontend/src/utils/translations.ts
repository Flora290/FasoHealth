export type Language = 'en' | 'fr' | 'moore' | 'dioula';
export type TranslationKey = keyof typeof translations.en;

// Hook to use translations
export const useTranslation = (language: Language = 'en') => {
  const t = (key: TranslationKey): string => {
    // @ts-ignore - Some languages might be partially translated
    return translations[language]?.[key] || translations.en[key] || key;
  };

  const changeLanguage = (newLanguage: Language) => {
    localStorage.setItem('language', newLanguage);
    window.location.reload();
  };

  return { t, changeLanguage, currentLanguage: language };
};

// Helper to detect preferred language
export const getPreferredLanguage = (): Language => {
  const saved = localStorage.getItem('language') as Language;
  if (saved && ['en', 'fr', 'moore', 'dioula'].includes(saved)) {
    return saved;
  }
  
  // Detect browser language
  const browserLang = typeof navigator !== 'undefined' ? navigator.language.toLowerCase() : 'en';
  if (browserLang.includes('en')) return 'en';
  if (browserLang.includes('fr')) return 'fr';
  
  return 'en'; // Default to English as per user request
};

// Helper for formatting dates by language
export const formatDate = (date: Date, language: Language = 'en'): string => {
  const options: Intl.DateTimeFormatOptions = {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  };

  try {
    if (language === 'en') {
      return date.toLocaleDateString('en-US', options);
    } else if (language === 'fr') {
      return date.toLocaleDateString('fr-FR', options);
    } else {
      return date.toLocaleDateString('en-US', options);
    }
  } catch {
    return date.toLocaleDateString('en-US', options);
  }
};

// Helper for formatting amounts by language
export const formatCurrency = (amount: number, language: Language = 'en'): string => {
  try {
    if (language === 'en') {
      return new Intl.NumberFormat('en-US', {
        style: 'currency',
        currency: 'USD'
      }).format(amount);
    } else if (language === 'fr') {
      return new Intl.NumberFormat('fr-FR', {
        style: 'currency',
        currency: 'XOF'
      }).format(amount);
    } else {
      return `${amount.toLocaleString()} FCFA`;
    }
  } catch {
    return `${amount} FCFA`;
  }
};

